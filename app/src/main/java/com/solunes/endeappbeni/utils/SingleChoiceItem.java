public class SingleChoiceItem {
    private int code;
    private String title;
    private int obsInd;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getObsInd() {
        return obsInd;
    }

    public void setObsInd(int obsInd) {
        this.obsInd = obsInd;
    }
}
